class PositionEmbed(nn.Module):
    def __init__(self, channel_num, token_num):
        super(PositionEmbed, self).__init__()
        self.position_embed = nn.Parameter(torch.zeros(1, token_num, channel_num))
        nn.init.trunc_normal_(self.position_embed, std=0.02)

    def forward(self, x):
        x = x + self.position_embed
        return x